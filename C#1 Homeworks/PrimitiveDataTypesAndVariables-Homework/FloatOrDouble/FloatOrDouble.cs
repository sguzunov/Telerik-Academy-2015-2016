using System;

class FloatOrDouble
{
    static void Main()
    {
        float f1 = 12.345f;
        float f2 = 3456.091f;
        double d1 = 34.567839023;
        double d2 = 8923.1234857;
        Console.WriteLine("Float numbers:{0}   {1}",f1,f2);
        Console.WriteLine("Double numbers:{0}   {1}",d1,d2);
        Console.WriteLine();

    }
}

